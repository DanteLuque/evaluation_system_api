# ¿Cuántos alumnos desaprobados tenemos en total?
SELECT COUNT(DISTINCT ID_ALUMNO) AS ALUMNOS_DESAPROBADOS 
FROM DESARROLLO_EVALUACION WHERE CALIFICACION<13;



# ¿Cuántos alumnos aprobados en un determinado curso tenemos?
/*SELECT D_E.ID_ALUMNO, D_E.CALIFICACION, E.ID AS ID_EVALUACION, E.ID_AREA,A.NOMBRE_AREA*/
SELECT COUNT(DISTINCT D_E.ID_ALUMNO) AS ALUMNOS_DESAPROBADOS
FROM DESARROLLO_EVALUACION AS D_E
INNER JOIN EVALUACIONES AS E ON D_E.ID_EVALUACION = E.ID
INNER JOIN AREAS AS A ON E.ID_AREA = A.ID
WHERE D_E.CALIFICACION>=13 AND NOMBRE_AREA='JAVASCRIPT';
# AREAS => PYTHON, JAVA, PHP, C#, JAVASCRIPT




# ¿A cuántos exámenes está inscrito un alumno y cuántos de ellos están resueltos y pendientes?
SELECT 
COUNT(ID_EVALUACION) AS EXAMENES_INSCRITOS,
SUM(CASE WHEN ESTA_DESARROLLADO = 1 THEN 1 ELSE 0 END) AS RESUELTOS,
SUM(CASE WHEN ESTA_DESARROLLADO = 0 THEN 1 ELSE 0 END) AS PENDIENTES
FROM DESARROLLO_EVALUACION
WHERE ID_ALUMNO = 4;




# ¿Cuál es la mejor y peor calificación de una determinada evaluación?
-- SELECT D_E.ID_ALUMNO, D_E.CALIFICACION, E.ID AS ID_EVALUACION, E.ID_AREA,A.NOMBRE_AREA
SELECT MAX(CALIFICACION), MIN(CALIFICACION)
FROM DESARROLLO_EVALUACION AS D_E
INNER JOIN EVALUACIONES AS E ON D_E.ID_EVALUACION = E.ID
INNER JOIN AREAS AS A ON E.ID_AREA = A.ID
WHERE D_E.ID_EVALUACION=3;




# ¿Cómo calcularíamos el promedio de calificaciones de un estudiante?
-- SELECT * FROM DESARROLLO_EVALUACION WHERE ID_ALUMNO=3;
SELECT AVG(CALIFICACION) FROM DESARROLLO_EVALUACION WHERE ID_ALUMNO=3;